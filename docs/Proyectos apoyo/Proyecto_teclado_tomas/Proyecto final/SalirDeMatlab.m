                  'Position',[50 25 80 40]  , ...
                  'CallBack',AccionSi, ...
                  'FontSize',16,...
                  'String','Si'  , ...
                  'TooltipString', 'Saldra de Matlab y se cerraran todas las ventanas', ...
                  'HorizontalAlignment','center'   , ...
                  'Tag','SiButton');
             
NoHandle=uicontrol(figs(2), Font, ...
